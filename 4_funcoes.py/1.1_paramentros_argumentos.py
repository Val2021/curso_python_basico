'''
PARAMETROS E ARGUMENTOS
'''

'''
Em programação, os termos "parâmetros" e "argumentos" estão relacionados ao uso de funções.
Eles se referem aos valores que são passados para uma função durante sua chamada
'''

'''
Exemplo de parametro:
'''

def saudacao(nome):
    print("Olá,", nome)

'''
Exemplo de argumento:
'''

def saudacao(nome):
    print("Olá,", nome)

saudacao("João")